# All seed templates (4 student + 5 company = 9).
TEMPLATE_SEEDS: list[dict] = _STUDENT_TEMPLATES + _COMPANY_TEMPLATES


async def seed_templates(session: AsyncSession) -> int:
    """Insert any missing catalog templates, idempotently by (category, name).

    Returns the number of templates inserted. Existing templates (matched on
    ``category`` + ``name``) are left untouched so reseeding is safe.
    """
    inserted = 0
    for spec in TEMPLATE_SEEDS:
        existing = await session.execute(
            select(Template.id).where(
                Template.category == spec["category"],
                Template.name == spec["name"],
            )
        )
        if existing.first() is not None:
            continue
        session.add(Template(**spec))
        inserted += 1
    if inserted:
        await session.commit()
    return inserted

# ---------------------------------------------------------------------------
# Reusable Arduino sketch skeletons (kept short; real firmware is generated by
# the Web Flasher pipeline). Each ends with a publish to the device's telemetry
# topic, matching the MQTT contract iotaps/{org_id}/{device_id}/telemetry.
# ---------------------------------------------------------------------------
def _arduino_sketch(sensor_comment: str, read_body: str) -> str:
    """Build a minimal ESP32 sketch publishing a JSON telemetry payload."""
    return (
        "#include <WiFi.h>\n"
        "#include <PubSubClient.h>\n"
        "\n"
        "// " + sensor_comment + "\n"
        "WiFiClient espClient;\n"
        "PubSubClient mqtt(espClient);\n"
        "\n"
        "void setup() {\n"
        "  Serial.begin(115200);\n"
        "  WiFi.begin(\"SSID\", \"PASSWORD\");\n"
        "  mqtt.setServer(\"BROKER_HOST\", 1883);\n"
        "}\n"
        "\n"
        "void loop() {\n"
        "  if (!mqtt.connected()) mqtt.connect(\"DEVICE_ID\", \"MQTT_USER\", \"MQTT_PASS\");\n"
        + read_body
        + "  delay(60000); // default 60s interval\n"
        "}\n"
    )


